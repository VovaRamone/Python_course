# В фермерском хозяйстве в Карелии выращивают чернику.
# Она растёт на круглой грядке, причём кусты высажены только по окружности.
# Таким образом, у каждого куста есть ровно два соседних. Всего на грядке растёт N кустов.
# Эти кусты обладают разной урожайностью, поэтому ко времени сбора на них выросло различное число ягод
# — на i-ом кусте выросло ai ягод.
# В этом фермерском хозяйстве внедрена система автоматического сбора черники.
# Эта система состоит из управляющего модуля и нескольких собирающих модулей.
# Собирающий модуль за один заход, находясь непосредственно перед некоторым кустом,
# собирает ягоды с этого куста и с двух соседних с ним.
# Напишите программу для нахождения максимального числа ягод,
# которое может собрать за один заход собирающий модуль,
# находясь перед некоторым кустом заданной во входном файле грядки.


import random

n = int(input("Введите количество кустов: "))

# генерируем случайное количество ягод для каждого куста
berries = [random.randint(1, 10) for i in range(n)]

# находим максимальную сумму ягод среди троек кустов
max_sum = 0
max_triplet = []
for i in range(n):
    triplet = [berries[i], berries[(i+1)%n], berries[(i+2)%n]]
    triplet_sum = sum(triplet)
    if triplet_sum > max_sum:
        max_sum = triplet_sum
        max_triplet = triplet

# выводим результаты
print("Количество ягод на каждом кусте: ", berries)
print("Три куста с максимальной суммой ягод: ", max_triplet, "сумма: ", max_sum)


