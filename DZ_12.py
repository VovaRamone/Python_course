# Петя и Катя – брат и сестра. Петя – студент, а Катя – школьница.
# Петя помогает Кате по математике. Он задумывает два натуральных числа X и Y (X,Y≤1000),
# а Катя должна их отгадать. Для этого Петя делает две подсказки.
# Он называет сумму этих чисел S и их произведение P. Помогите Кате отгадать задуманные Петей числа.


# Просим юзера ввести сумму и произведение двух чисел
sum_xy = int(input("Enter the sum of the two numbers: "))
prod_xy = int(input("Enter the product of the two numbers: "))

# Инициализируем переменные
x = 1
y = prod_xy

# Перебираем все возможные значения x и y, чтобы найти те, которые соответствуют сумме и произведению
while x <= y:
    if x + y == sum_xy and x * y == prod_xy:
        # Выводим в консоль два числа соответствующих заданным значениям и прерываем цикл
        print("The two numbers are:", x, "and", y)
        break
    x += 1
    y = prod_xy // x

# Если нет совпадений, выводим сообщение об ошибке.
if x > y:
    print("No matching numbers found!")
